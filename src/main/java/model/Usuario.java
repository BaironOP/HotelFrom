package model;

/**
 *
 * @author Usuario
 */
public class Usuario {
    
    private String nombre;
    private String id;
    private int edad;
    private Habitacion habitacion;
    

    public Usuario() {
    }

    public Usuario(String nombre, String id, int edad, Habitacion habitacion) {
        this.nombre = nombre;
        this.id = id;
        this.edad = edad;
        this.habitacion = habitacion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public Habitacion getHabitacion() {
        return habitacion;
    }

    public void setHabitacion(Habitacion habitacion) {
        this.habitacion = habitacion;
    }

    
}
